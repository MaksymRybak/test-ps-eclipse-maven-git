package com.pluralsight.model;

public class User {
    
    /*
     * User's name
     */
    private String name;
    
    /*
     * User's age
     */
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
}
